create or replace TRIGGER TRG_TBL_FUTSPNSRONBRDING_ENCRY
BEFORE INSERT OR UPDATE ON TBL_FUTURESPONSORONBOARDING
FOR EACH ROW
DECLARE
BEGIN

IF NVL(:OLD.FIRSTNAME,'!@#$') <> NVL(:NEW.FIRSTNAME,'!@#$') THEN
  :NEW.FIRSTNAME := PKG_ENCRYPT.FN_ENCRYPT(:NEW.FIRSTNAME);
END IF;

IF NVL(:OLD.MIDDLENAME,'!@#$') <> NVL(:NEW.MIDDLENAME,'!@#$') THEN
  :NEW.MIDDLENAME := PKG_ENCRYPT.FN_ENCRYPT(:NEW.MIDDLENAME);
END IF;

IF NVL(:OLD.LASTNAME,'!@#$') <> NVL(:NEW.LASTNAME,'!@#$') THEN
  :NEW.LASTNAME := PKG_ENCRYPT.FN_ENCRYPT(:NEW.LASTNAME);
END IF;

IF NVL(:OLD.ADDRESS1,'!@#$') <> NVL(:NEW.ADDRESS1,'!@#$') THEN
  :NEW.ADDRESS1 := PKG_ENCRYPT.FN_ENCRYPT(:NEW.ADDRESS1);
END IF;

IF NVL(:OLD.ADDRESS2,'!@#$') <> NVL(:NEW.ADDRESS2,'!@#$') THEN
  :NEW.ADDRESS2 := PKG_ENCRYPT.FN_ENCRYPT(:NEW.ADDRESS2);
END IF;

IF NVL(:OLD.ADDRESS3,'!@#$') <> NVL(:NEW.ADDRESS3,'!@#$') THEN
  :NEW.ADDRESS3 := PKG_ENCRYPT.FN_ENCRYPT(:NEW.ADDRESS3);
END IF;

IF NVL(:OLD.POSTALCODE,'!@#$') <> NVL(:NEW.POSTALCODE,'!@#$') THEN
  :NEW.POSTALCODE := PKG_ENCRYPT.FN_ENCRYPT(:NEW.POSTALCODE);
END IF;

IF NVL(:OLD.PHONE1,'!@#$') <> NVL(:NEW.PHONE1,'!@#$') THEN
  :NEW.PHONE1 := PKG_ENCRYPT.FN_ENCRYPT(:NEW.PHONE1);
END IF;

IF NVL(:OLD.EMAIL,'!@#$') <> NVL(:NEW.EMAIL,'!@#$') THEN
  :NEW.EMAIL := PKG_ENCRYPT.FN_ENCRYPT(:NEW.EMAIL);
END IF;
END TRG_TBL_FUTSPNSRONBRDING_ENCRY;

/